    public String changePassword(int userId, String oldPassword, String newPassword) {
        if (oldPassword == null || oldPassword.trim().isEmpty()) {
            return "原密码不能为空";
        }
        if (newPassword == null || newPassword.trim().isEmpty()) {
            return "新密码不能为空";
        }

        User user = userMapper.selectById(userId);
        // 安全比对：加密用户输入的旧密码
        String encryptedOldInput = CommunityUtil.md5(oldPassword + user.getSalt());
        if (!user.getPassword().equals(encryptedOldInput)) {
            return "原密码输入错误";
        }

        // 更新密码（加密存储）
        String newEncrypted = CommunityUtil.md5(newPassword + user.getSalt());
        user.setPassword(newEncrypted);
        userMapper.updatePassword(user.getId(),newEncrypted);// 确保有这个更新方法

        return null; // 成功
    }
